package tallerUno.Payan;

import processing.core.PConstants;
import processing.core.PImage;

public class Pick {
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////

	private Main app;
	private int x, y, selector;
	private String tipo;
	private PImage hover;

/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////

	public Pick(Main app, int x, int y) {
		this.app = app;
		this.x = x;
		this.y = y;
		selector = (int) app.random(0, 3);

		switch (selector) {

		case 0:
			tipo = "Suelo";
			hover = app.loadImage("./data/Escenario/Suelo/tierra.png");
			break;

		case 1:
			tipo = "Mina";
			hover = app.loadImage("./data/Escenario/Armas/mina1.png");
			;
			break;

		case 2:
			tipo = "Andamio";
			hover = app.loadImage("./data/Escenario/Armas/andamio.png");
			break;

		}// cierra el switch de selector
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
	}// cierra el constructor de pick

	/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
	public void pintar() {

		app.imageMode(PConstants.CENTER);
		app.image(hover, x, y);

	}// cierra el metodo pintar
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////

	// getters and setters

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public PImage getHover() {
		return hover;
	}

	public String getTipo() {
		return tipo;
	}

/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////

}// cierra la clase Pick
