package tallerUno.Payan;

import processing.core.PImage;
import processing.core.PVector;

/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
public class Cuadricula {

	private Main app;
	private PVector pos;
	private int ancho;
	private int alto;
	private boolean ocupado;
	private PImage cuadroD;
	private PImage cuadroO;

/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
	public Cuadricula(Main app, int x, int y) {
		this.app = app;
		pos = new PVector(x, y);
		ancho = 100;
		alto = 100;
		ocupado = false;
		cuadroD = app.loadImage("./data/Escenario/Iconos/desocupado.png");
		cuadroO = app.loadImage("./data/Escenario/Iconos/ocupado.png");
	}// cierra el constructo de cuadricula
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////

	public void pintar() {

		if (ocupado == false) {
			app.image(cuadroD, pos.x + ancho / 2, pos.y + alto / 2);
		}

		else {
			app.image(cuadroO, pos.x + ancho / 2, pos.y + alto / 2);
		}
	} // cierra el metodo pintar
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////

	// getters and setters

	public PVector getPos() {
		return pos;
	}

	public int getAncho() {
		return ancho;
	}

	public int getAlto() {
		return alto;
	}

	public boolean isOcupado() {
		return ocupado;
	}

	public void setOcupado(boolean ocupado) {
		this.ocupado = ocupado;
	}
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
}// cierra la clase cuadricula
