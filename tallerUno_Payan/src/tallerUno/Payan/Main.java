package tallerUno.Payan;

import processing.core.PApplet;

public class Main extends PApplet {

	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////

	public static void main(String[] args) {
		PApplet.main("tallerUno.Payan.Main");

	}// cierra el constructor de main

	private Logica log;
	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////

	public void settings() {
		size(1200, 700);
	}// cierra el metodo settings

	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////

	public void setup() {

		
		frameRate(60);
		log = new Logica(this);
	}// cierra el metodo setup

	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////

	public void draw() {
		noCursor();
		log.pintar();
	} // cierra el metodo draw

	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////

	public void mouseClicked() {

		log.click();

	} // cierra el metodo mouseClicked

	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////

	public void keyPressed() {

		log.presionarTeclas();

	}// cierra el metodo keypressed

	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////

	public void keyReleased() {

		log.soltarTeclas();

	}// cierra el metodo keyReleased

	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////////

}// cierra la clase Main
